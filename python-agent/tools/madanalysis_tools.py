def madanalysis_process(
    events_path: str,
    script: str,
    target_path: str,
    level: str = "parton",
) -> Dict[str, Any]:
    """
    Run MadAnalysis5 analysis.

    Takes the process directory from madgraph_launch (containing Events/)
    and runs a MadAnalysis5 script to produce plots, cutflows, and reports.

    Args:
        events_path: Process directory from madgraph_launch (contains Events/).
        script: MA5 commands with {EVENTS_DIR} placeholder for event file paths.
            The placeholder is replaced with the actual path to the events directory.
            Do NOT include a `submit` command — it is appended automatically.
            Any user-written `submit` lines are stripped before execution.
            Example: "import {EVENTS_DIR}/Events/run_01/unweighted_events.lhe.gz as signal"
        target_path: Where to download the analysis output directory.
        level: Analysis level - "parton" (LHE), "hadron" (HepMC), "reco" (LHCO/ROOT).
            Default: "parton"

    Returns:
        dict with keys:
            - success (bool): Whether analysis succeeded
            - output_dir (str): Path to analysis output directory (if success)
            - message (str): Human-readable status message

    Example:
        >>> result = madanalysis_process(
        ...     events_path="tmp/pp_wprime",
        ...     script="import {EVENTS_DIR}/Events/run_01/unweighted_events.lhe.gz as wprime\\n"
        ...            "set wprime.type = signal\\n"
        ...            "plot PT(mu+) 50 0 500",
        ...     target_path="tmp/ma5_output",
        ...     level="parton",
        ... )
        >>> if result["success"]:
        ...     print(f"Analysis output at: {result['output_dir']}")
    """

    try:
        args = {
            "events": events_path,
            "script": script,
            "level": level,
            "output": target_path,
        }

        result = magnus.run_blueprint("madanalysis-process", args=args)
        assert result is not None
        return json.loads(result)
    except:
        return {
            "success": False,
            "error": traceback.format_exc(),
        }
