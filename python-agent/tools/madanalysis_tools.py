def connect_to_madanalysis(
    script: str,
    level: str = "reco",
    timeout: int = 600,
    ma5_path: Optional[str] = None,
) -> Dict[str, Any]:
    """
    Execute MadAnalysis5 normal-mode commands.

    This is the main interface to MadAnalysis5. It accepts either:
    1. A multi-line string of MA5 commands
    2. A path to an existing .ma5 script file

    Args:
        script: MadAnalysis5 commands (string) or path to .ma5 script file
        level: Analysis level - "parton" (-P), "hadron" (-H), or "reco" (-R)
                Default: "reco"
        timeout: Maximum execution time in seconds (default: 600 = 10 minutes)
        ma5_path: Path to ma5 executable (optional, uses MA5_PATH env var)

    Returns:
        dict: {
            "success": bool,
            "stdout": str,
            "stderr": str,
            "return_code": int,
            "message": str
        }

    Analysis Levels:
    ----------------
    - "parton" (-P): For LHE files (no showering). No jet clustering.
    - "hadron" (-H): For HepMC files (showered events). FastJet available.
    - "reco"   (-R): For LHCO/ROOT files (detector-simulated). Reconstructed objects.

    Normal Mode Workflow:
    --------------------
    1. import <filepath> [as <name>]   - Load event files
    2. set <dataset>.type = signal     - Configure dataset type
    3. set main.lumi = 100             - Set luminosity
    4. plot PT(mu) 50 0 500            - Define histograms
    5. select N(j) >= 2                - Apply event selections
    6. submit <output_name>            - Run analysis and produce reports

    Example - Basic reco-level analysis:
        script = '''
        import /path/to/events.lhco.gz as signal
        set signal.type = signal
        set main.lumi = 100
        plot MET 50 0 500
        select N(j) >= 2
        submit my_analysis
        '''
        result = connect_to_madanalysis(script, level="reco")

    For complete reference, see prompts/madanalysis_reference.md
    """
    # Find MadAnalysis5 executable
    ma5_executable = _find_ma5_executable(ma5_path)

    if not ma5_executable:
        return {
            "success": False,
            "stdout": "",
            "stderr": "",
            "return_code": -1,
            "message": "MadAnalysis5 not found. Set MA5_PATH environment variable.",
        }

    # Map level to command-line flag
    level_flags = {
        "parton": "-P",
        "hadron": "-H",
        "reco": "-R",
    }
    level_flag = level_flags.get(level.lower())
    if not level_flag:
        return {
            "success": False,
            "stdout": "",
            "stderr": "",
            "return_code": -1,
            "message": (
                f"Invalid level '{level}'. Use 'parton', 'hadron', or 'reco'."
            ),
        }

    logger.info(f"Executing MadAnalysis5 in {level} mode...")

    result = {
        "success": False,
        "stdout": "",
        "stderr": "",
        "return_code": -1,
        "message": "",
    }

    try:
        # Check if script is a file path or command string
        script_path = Path(script.strip())

        if script_path.exists() and script_path.suffix == ".ma5":
            logger.info(f"Running script file: {script_path}")
            script_file = str(script_path)
            cleanup_script = False
        else:
            # Write commands to temp file
            with tempfile.NamedTemporaryFile(
                mode="w", suffix=".ma5", delete=False
            ) as f:
                f.write(script)
                script_file = f.name
            cleanup_script = True
            logger.debug(f"Script written to {script_file}")

        # Execute MadAnalysis5: ./bin/ma5 <level_flag> -s <script>
        # -s flag: auto-quit after script execution
        cmd = [str(ma5_executable), level_flag, "-s", script_file]

        proc = subprocess.run(
            cmd,
            capture_output=True,
            text=True,
            timeout=timeout,
        )

        result["stdout"] = proc.stdout
        result["stderr"] = proc.stderr
        result["return_code"] = proc.returncode
        result["success"] = proc.returncode == 0

        if result["success"]:
            result["message"] = "MadAnalysis5 execution completed successfully"
            logger.info(result["message"])
        else:
            result["message"] = (
                f"MadAnalysis5 failed with return code {proc.returncode}"
            )
            logger.error(f"MadAnalysis5 error: {proc.stderr}")

        # Cleanup temp file
        if cleanup_script:
            os.unlink(script_file)

    except subprocess.TimeoutExpired:
        result["message"] = (
            f"MadAnalysis5 execution timed out after {timeout} seconds"
        )
        logger.error(result["message"])
    except Exception as e:
        result["message"] = f"MadAnalysis5 execution error: {e}"
        logger.error(result["message"], exc_info=True)

    return result


def _find_ma5_executable(ma5_path: Optional[str] = None) -> Optional[Path]:
    """Find MadAnalysis5 executable."""
    # Priority: argument > environment variable
    path = ma5_path or os.getenv("MA5_PATH")

    if not path:
        return None

    ma5_bin = Path(path)

    # Handle both directory and direct executable path
    if ma5_bin.is_dir():
        executable = ma5_bin / "ma5"
    else:
        executable = ma5_bin

    if executable.exists():
        return executable

    return None


