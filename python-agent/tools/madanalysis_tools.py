def read_event_index(
    yaml_path: str,
    process_name: str,
    event_set_name: str = "14TeV",
) -> Dict[str, Any]:
    """
    Read event_index.yaml and return event file paths for a given process.

    The event_index.yaml is auto-generated by the simulation step and maps
    process names to their event file locations.

    Args:
        yaml_path: Path to event_index.yaml
        process_name: Process key (e.g. "pp_to_tt_3l", "pp_to_tS_3l_decay")
        event_set_name: Energy label (default "14TeV")

    Returns:
        dict: {
            "success": bool,
            "process_name": str,
            "base_path": str,          # Relative path of the process
            "runs": list[dict],         # List of run entries with path, nevents, etc.
            "event_files": list[str],   # Resolved event file paths (auto-detected)
            "message": str
        }

    Event file auto-detection order per run directory:
        1. tag_*_delphes_events.lhco.gz  (reco-level, LHCO)
        2. tag_*_delphes_events.root     (reco-level, ROOT)
        3. tag_*_pythia8_events.hepmc.gz (hadron-level)
        4. unweighted_events.lhe.gz      (parton-level)
    """
    result = {
        "success": False,
        "process_name": process_name,
        "base_path": "",
        "runs": [],
        "event_files": [],
        "message": "",
    }

    try:
        with open(yaml_path, "r") as f:
            data = yaml.safe_load(f)

        if not data or process_name not in data:
            available = list(data.keys()) if data else []
            result["message"] = (
                f"Process '{process_name}' not found in {yaml_path}. "
                f"Available: {available}"
            )
            return result

        # Find the matching event set
        process_block = data[process_name]
        if not isinstance(process_block, list):
            result["message"] = f"Unexpected format for process '{process_name}'"
            return result

        config = None
        for entry in process_block:
            if isinstance(entry, dict) and event_set_name in entry:
                config = entry[event_set_name]
                break

        if config is None:
            result["message"] = (
                f"Event set '{event_set_name}' not found for process '{process_name}'"
            )
            return result

        result["base_path"] = config.get("path", "")
        runs = config.get("runs", [])
        result["runs"] = runs

        # Auto-detect event files for each run
        event_files = []
        for run in runs:
            run_path = run.get("path", "")
            if not run_path:
                continue
            found = _find_event_file(run_path)
            if found:
                event_files.append(found)

        result["event_files"] = event_files
        result["success"] = True
        result["message"] = (
            f"Found {len(runs)} run(s) for {process_name}/{event_set_name}, "
            f"{len(event_files)} event file(s) detected"
        )
        logger.info(result["message"])

    except Exception as e:
        result["message"] = f"Error reading event index: {e}"
        logger.error(result["message"], exc_info=True)

    return result


def _find_event_file(run_dir: str) -> Optional[str]:
    """Find the best event file in a run directory, preferring higher-level formats."""
    search_patterns = [
        "tag_*_delphes_events.lhco.gz",
        "tag_*_delphes_events.lhco",
        "tag_*_delphes_events.root",
        "tag_*_pythia8_events.hepmc.gz",
        "tag_*_pythia8_events.hepmc",
        "unweighted_events.lhe.gz",
        "unweighted_events.lhe",
    ]
    for pattern in search_patterns:
        matches = glob.glob(os.path.join(run_dir, pattern))
        if matches:
            return matches[0]
    return None


